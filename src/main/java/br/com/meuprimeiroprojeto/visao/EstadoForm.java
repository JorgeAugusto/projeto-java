    private void limparCampos() {
        tfNome.setText("");
        tfSigla.setText("");
    }

    private void habilita(Boolean b) {
        tfNome.setEnabled(b);
        tfSigla.setEnabled(b);
    }

    private void salvar() {
        EstadoDao dao = new EstadoDaoImpl();
        estado.setNome(tfNome.getText().trim());
        estado.setSigla(tfSigla.getText().trim());
        dao.save(estado);
        JOptionPane.showMessageDialog(null, "Salvo com Sucesso!");
    }

    private void excluir() {
        EstadoDao dao = new EstadoDaoImpl();
        estado = dao.getById(estado.getIdEstado());
        dao.delete(estado);
    }
